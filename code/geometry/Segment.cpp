CPoint Segment::GetClosestPoint( const CPoint &P ) const
{
	float t = m_P1_sub_P0.Dot( P ^ m_P[ 0 ] ) / m_LengthSquared;

	t = ntstd::Max( ntstd::Min( t, 1.0f ), 0.0f );	// Clamp to [0,1].

	return m_P[ 0 ] + t * m_P1_sub_P0;
}





